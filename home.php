    <script>
    const observer = new IntersectionObserver((entries) => {

        entries.forEach((entry) => {

            if(entry.isIntersecting){
                entry.target.classList.add("show");
            }

        });

    },{
        threshold:0.2
    });

    document.querySelectorAll(".fade-up").forEach((el)=>{
        observer.observe(el);
    });

    const words = [
    "Marrakech",
    "Agafay",
    "Adventure",
    "Transport",
    "Excursions",
    "shows"
        ];

        const kech = document.querySelector(".kech");

        let wordIndex = 0;
        let charIndex = 0;
        let deleting = false;

        function typeEffect(){

            const currentWord = words[wordIndex];

            if(!deleting){

                kech.textContent = currentWord.substring(0,charIndex+1);
                charIndex++;

                if(charIndex === currentWord.length){

                    deleting = true;
                    setTimeout(typeEffect,1500);
                    return;
                }

            }else{

                kech.textContent = currentWord.substring(0,charIndex-1);
                charIndex--;

                if(charIndex === 0){

                    deleting = false;
                    wordIndex++;

                    if(wordIndex >= words.length){
                        wordIndex = 0;
                    }
                }
            }

            setTimeout(typeEffect,deleting ? 70 : 120);
        }

        typeEffect();


    </script>
